public class Kahoot1 {
    public static void main(String[] args) {


        for (int i = 1; i <= 10;i++){
            System.out.print(i + " ");
            i++;
        }

        System.out.println();

        for (int i = 1; i <= 10;i++){
            i++;
            System.out.print(i + " ");
        }
        System.out.println();

        //Print out all odd number from 1 - 9

        for (int i = 1 ; i <= 9; i+=2){
            System.out.print(i + " ");
        }


    }
}
